            var videoSrc = button.getAttribute('data-video-src'); // Extract info from data-* attributes
            var iframe = videoModal.querySelector('#videoIframe');
            iframe.src = videoSrc;
        });

        videoModal.addEventListener('hidden.bs.modal', function () {
            var iframe = videoModal.querySelector('#videoIframe');
            iframe.src = ''; // Stop the video when the modal is closed
        });
    });
</script>

